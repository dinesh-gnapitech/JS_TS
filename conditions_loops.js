// -----------------------------------
// Conditional Statement (if-else)
// -----------------------------------

let a = 12;  // Try changing this to test the condition

if (a > 10) {
  console.log("Greater than 10");
} else {
  console.log("Not greater");
}
// Explanation: If the value of 'a' is greater than 10, it prints "Greater than 10". Otherwise, it prints "Not greater".


// -----------------------------------
// For Loop
// -----------------------------------

// This loop will print numbers from 0 to 4
for (let i = 0; i < 5; i++) {
  console.log("For loop i =", i);
}
// Explanation: Initializes i=0, checks i<5, then prints and increments i. Repeats until i is 5.


// -----------------------------------
// While Loop
// -----------------------------------

// This loop also prints numbers from 0 to 4
let j = 0;
while (j < 5) {
  console.log("While loop j =", j);
  j++;
}
// Explanation: Starts at j=0, checks condition j<5, runs the block and increments j until j is 5.
